const mergeActivityData = async (defaultActivityData, userId) => {
  try {
    // get the backend data
    const backendData = await getActivityByUserId(userId);

    // Convert backend data to lookup map for efficiency
    const activityMap = {};

    backendData.forEach((item) => {
      activityMap[item.date] = item.count;
    });

    // Fill in actual counts where they exist
    defaultActivityData.forEach((dayData) => {
      dayData.count = activityMap[dayData.date] || 0;
    });

    return defaultActivityData;
  } catch (error) {
    console.error(
      "Error while fetching the activity of the user: ",
      userId,
      " : ",
      error
    );
  }
};

